n = int(input())
if n == 0:
    print()
elif n == 1:
    print('*')
elif n == 2:
    print('**')
else:
    if n % 2 == 1:
        print('-' * ((n - 1) // 2) + '*' + '-' * ((n - 1) // 2))
    else:
        print('-' * ((n - 1) // 2) + '**' + '-' * ((n - 1) // 2))
    for i in range(1, (n - 1) // 2 + 1):
        if n % 2 == 1:
            print('-' * ((n - 1) // 2 - i) + '*' + '-' * (2 * i - 1) + '*' + '-' * ((n - 1) // 2 - i))
        else:
            print('-' * ((n - 1) // 2 - i) + '*' + '-' * (2 *i) + '*' + '-' * ((n - 1) // 2 - i))
    for j in range(1, (n - 1) // 2):
        if n % 2 == 1:
            print('-' * j + '*' + '-' * (n - 2 - 2 * j) + '*' + '-' * j)
        else:
            print('-' * j+ '*' + '-' * (n - 2 - 2 * j) + '*' + '-' * j)
    if n % 2 == 1:
        print('-' * ((n - 1) // 2) + '*' + '-' * ((n - 1) // 2))
    else:
        print('-' * ((n - 1) // 2) + '**' + '-' * ((n - 1) // 2))

