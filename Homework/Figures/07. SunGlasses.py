n = int(input())
print('*' * (n * 2) + ' ' * n + '*' * (n * 2))
for i in range(2, n):
    if n % 2 == 1:
        c = n - (n // 2)
        if i == c:
            print('*' + '/' * (2 * n - 2) + '*' + '|' * n + '*' + '/' * (2 * n - 2) + '*')
        else:
            print('*' + '/' * (2 * n - 2) + '*' + ' ' * n + '*' + '/' * (2 * n - 2) + '*')

    if n % 2 == 0:
        c = n // 2
        if i == c:
            print('*' + '/' * (2 * n - 2) + '*' + '|' * n + '*' + '/' * (2 * n - 2) + '*')
        else:
            print('*' + '/' * (2 * n - 2) + '*' + ' ' * n + '*' + '/' * (2 * n - 2) + '*')

print('*' * (n * 2) + ' ' * n + '*' * (n * 2))